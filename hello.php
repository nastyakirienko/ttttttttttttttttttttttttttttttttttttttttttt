<?php
// Создаем ассоциативный массив с информацией о товарах (название => количество)
$products = [
    "Книга" => 15,
    "Флешка" => 5,
    "Ручка" => 20,
    "Ноутбук" => 3,
];

// Находим товары, количество которых меньше 10 штук
echo "Товары, количество которых меньше 10 штук:\n";
foreach ($products as $product => $quantity) {
    if ($quantity < 10) {
        echo "$product: $quantity штук\n";
    }
}
?>





